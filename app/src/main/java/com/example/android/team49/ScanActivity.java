    private void toggleFlash(){
        int cameraSide = mCameraSource.getCameraFacing();
        if(mCameraSource != null) {
            mCameraSource.release();
        }

        flashImageView = (ImageView) findViewById(R.id.flash_image_view);
        useFlash = !useFlash;
        if(useFlash){
            flashImageView.setImageResource(R.drawable.ic_flash_on_black_24dp);
        } else {
            flashImageView.setImageResource(R.drawable.ic_flash_off_black_24dp);
        }
        createCameraSource(true, useFlash, cameraSide);
        startCameraSource();
        Log.d(TAG, "toggleFlash: " + useFlash);
    }

    private void toggleCamera(int newCameraSide){
        if(mCameraSource != null) {
            mCameraSource.release();
        }

        createCameraSource(true, false, newCameraSide);
        startCameraSource();
        Log.d(TAG, "toggleCamera");
    }

    /**
     * Handles the requesting of the camera permission.
     */
    private void requestCameraPermission() {
        Log.w(TAG, "Camera permission is not granted. Requesting permission");

        final String[] permissions = new String[]{android.Manifest.permission.CAMERA};

        if (!ActivityCompat.shouldShowRequestPermissionRationale(this,
                Manifest.permission.CAMERA)) {
            ActivityCompat.requestPermissions(this, permissions, RC_HANDLE_CAMERA_PERM);
            return;
        }

        final Activity thisActivity = this;

        View.OnClickListener listener = new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                ActivityCompat.requestPermissions(thisActivity, permissions,
                        RC_HANDLE_CAMERA_PERM);
            }
        };

        findViewById(R.id.topLayout).setOnClickListener(listener);
    }

